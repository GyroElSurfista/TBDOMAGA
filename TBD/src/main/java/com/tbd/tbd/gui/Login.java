    private void botonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonLoginMouseClicked
        VentanaIU ventana;
        try {
            ResultSet rs;
            int pid;
          
            c = Conexion.getConnection();
            
            CallableStatement stmnt = c.prepareCall("SELECT * FROM login(?,?) AS (pid INT4, usr INT4, sesion INT4)");
            
            stmnt.setString(1, userTextBox.getText());
            stmnt.setString(2,String.valueOf(contTextBox.getPassword()));
            
            stmnt.execute();
            rs = stmnt.getResultSet();
            
            if(rs.next()){
                pid = rs.getInt("pid");
            }else{
                pid = -1;
            }
            
            if(pid > 0){
                incLbl.setText("Login Exitoso PID: " + pid);
                usr.setIdUsr(rs.getInt("usr"));
                usr.setIdSesion(rs.getInt("sesion"));
                
                
            
                ventana = new VentanaIU(usr, c);
                ventana.setVisible(true);
                
            }else{
                incLbl.setText("Login Fallido");
        
            }
            
            incLbl.setVisible(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonLoginMouseClicked

