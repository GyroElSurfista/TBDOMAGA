    private void botonCloseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonCloseMouseClicked
        if(usr.valido()){
            
             try {

                CallableStatement stmnt = c.prepareCall("SELECT close(?,?)");

                stmnt.setInt(1, usr.getIdUsr());
                stmnt.setInt(2, usr.getIdSesion());

                stmnt.execute();

                c.close();
                
                usr.setIdUsr(-1);
                usr.setIdSesion(-1);
                
                incLbl.setText("Sesión cerrada");
                
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }
    }//GEN-LAST:event_botonCloseMouseClicked

